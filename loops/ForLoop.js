/* 
 Print the even number using for loop
*/

for (let i = 0; i < 10; i++) {
  if (i % 2 == 0) {
    console.log("Even Number: " + i);
  }
}

/* 
Print the Odd Number
*/

for (let i = 0; i < 10; i++) {
  if (i % 2 == 1) {
    console.log("Odd Number: " + i);
  }
}
